		The Women Entrepreneurship Training (WET) is a two-day intensive training with an emphasison of womenen etrepreneurs with limited business experiences and /or who are at the start up stage.</p><br>


<ul style="list-style-type:disc;">
<li><h1 style="color:indigo"><u><strong>Methodology</u></strong></h1></li>
<p style="font-size:25px">
The training methodology isapractical,hands-on, attitudinal and behavioral change training program built upon adult learning principles. Much of the facilitation is done by way of case studies,games,roleplay,exercisesand experientialsharing. The training is customized to meet the needs of women entrepreneurs with limited business experience.
 </p>
</br>
<li><h1 style="color:indigo"><u><strong>Training Content</u></strong></h1></li>
<p style="font-size:25px">
Participants are taken through a condensed behavioral and mind set change Programme that introduces them to the 10 Personal Entrepreneurial Competencies (PECs) and its 30 associated behaviors, identified with successful entrepreneurs worldwide.
Three entrepreneurial behaviors under each of the competency are learned and practiced by the participants during the workshop. In addition to the PECs, the training will equip women entrepreneurs with the skills necessary to increase their assertivenessin doing business. In addition, the training will address challenges that women entrepreneurs face and discuss possible solutions to help them address these challenges.
</p></br>
<li><h1 style="color:indigo"><u><strong>Benefits</u></strong></h1></li>
<p style="font-size:25px">
Upon successful completion of the WET, participants will be able to:

·         Recognize and adopt positive entrepreneurial behavior
·         Ensure the survival and grow their businesses
·         Understand the importance of setting realisticgoals and learn how to achievable them
·         Understand the market place and learn howto reachout to new and/or existing customers
·         Understand how to calculate business risks and develop propensity towards calculated risk taking
·         Appreciate customer loyalty and develop techniques geared towards customer satisfaction 
·         Equip themselves with skills to increase the efficiency and productivity of their enterprise
·         Understand the importance of planning
·         Develop negotiation skills 
·         Understand the importance of developing and maintaining business records 
·         Enhance the assertiveness and self-esteem of women entrepreneurs.
·         Sheds light on the unique challengesthat womenentrepreneurs may face and discuss possible solutions to help them address these challenges.
</p></br></br>
<li><h1 style="color:indigo"><u><strong>Target Participants</u></strong></h1></li>
<p style="font-size:25px">
This training targets women entrepreneurs with limited business experience who is operating an enterprise or an aspiring entrepreneur who is in the planning phase to establish an enterprise. Women entrepreneurs operating in any sector are able to enroll in the training Programme. Candidates are expected to have basic writing and reading skills.
The participants must be able to commit two consecutive full days (usually from 8:30AM – 5:30PM on the training dates). Being late or missing a session results in automatic disqualification of the participant from certification.
</p><br><br>
